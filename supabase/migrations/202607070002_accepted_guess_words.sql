-- Keep daily target words separate from the larger guess dictionary.
create table if not exists public.accepted_words (
  word text primary key,
  constraint accepted_words_normalized_check check (word = lower(btrim(word))),
  constraint accepted_words_six_letters_check check (char_length(word) = 6)
);

alter table public.accepted_words enable row level security;

drop policy if exists "Accepted words are publicly readable" on public.accepted_words;
create policy "Accepted words are publicly readable"
  on public.accepted_words
  for select
  to anon, authenticated
  using (true);

grant select on table public.accepted_words to anon, authenticated;

-- Every target word must always be accepted as a guess.
insert into public.accepted_words (word)
select lower(btrim(word)) from public.words
on conflict (word) do nothing;

